/** Busca full-text simples (case-insensitive) em title, authors, summary, tags */
export function searchKnowledge(
  items: KnowledgeItem[],
  query: string,
  filters?: {
    kind?: KnowledgeKind | "all"
    category?: string
    yearMin?: number
    yearMax?: number
  }
): KnowledgeItem[] {
  const q = query.trim().toLowerCase()
  return items.filter((it) => {
    // Filtro por tipo
    if (filters?.kind && filters.kind !== "all" && it.kind !== filters.kind) return false
    // Filtro por categoria
    if (filters?.category && it.category !== filters.category) return false
    // Filtro por ano
    if (filters?.yearMin && it.year && it.year < filters.yearMin) return false
    if (filters?.yearMax && it.year && it.year > filters.yearMax) return false
    // Busca textual
    if (!q) return true
    const hay = [
      it.title,
      it.authors ?? "",
      it.summary,
      it.category,
      it.source,
      it.doi ?? "",
      it.patentNumber ?? "",
      ...it.tags,
    ]
      .join(" ")
      .toLowerCase()
    return hay.includes(q)
  })
}

/** Lista categorias únicas para filtro */
export function getCategories(items: KnowledgeItem[]): string[] {
  return Array.from(new Set(items.map((i) => i.category))).sort()
}
